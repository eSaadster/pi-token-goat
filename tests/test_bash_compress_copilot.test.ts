it("test_copilot_registered_in_filters", () => {
  const names = FILTERS.map((f) => f.name);
  expect(names).toContain("copilot");
});

it("test_copilot_in_all_exports", () => {
  expect(bc.__all__).toContain("CopilotFilter");
});

it("test_copilot_filter_adjacent_to_gh_copilot_filter", () => {
  // CopilotFilter should immediately follow GhCopilotFilter in FILTERS.
  const names = FILTERS.map((f) => f.name);
  const gh_idx = names.indexOf("gh-copilot");
  const cop_idx = names.indexOf("copilot");
  expect(cop_idx).toBe(gh_idx + 1);
});

// ---------------------------------------------------------------------------
// detect_from_command routing
// ---------------------------------------------------------------------------

it("test_dispatch_routes_copilot_standalone", () => {
  const result = detect_from_command("copilot explain 'git stash'");
  expect(result).not.toBeNull();
  const [f] = result!;
  expect(f.name).toBe("copilot");
});

it("test_dispatch_does_not_route_gh_copilot_to_standalone", () => {
  const result = detect_from_command("gh copilot explain 'git stash'");
  expect(result).not.toBeNull();
  const [f] = result!;
  expect(f.name).toBe("gh-copilot");
});

