describe("TestGhCopilotSuggestWithTarget", () => {
  // GhCopilotFilter should keep the 'Suggested command:' header.
  const argv = ["gh", "copilot", "suggest", "--target", "shell", "list files"];

  it("test_keeps_suggested_command_header", () => {
    const f = new GhCopilotFilter();
    const result = f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, argv);
    expect(result).toContain("Suggested command:");
  });

  it("test_keeps_the_actual_command", () => {
    const f = new GhCopilotFilter();
    const result = f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, argv);
    expect(result).toContain("git stash");
  });

  it("test_drops_welcome_banner", () => {
    const f = new GhCopilotFilter();
    const result = f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, argv);
    expect(result).not.toContain("Welcome to GitHub Copilot");
  });

  it("test_drops_spinner", () => {
    const f = new GhCopilotFilter();
    const result = f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, argv);
    expect(result).not.toContain("Asking GitHub Copilot");
  });

  it("test_drops_disclaimer", () => {
    const f = new GhCopilotFilter();
    const result = f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, argv);
    expect(result).not.toContain("Disclaimer:");
    expect(result).not.toContain("Always review");
  });
});

