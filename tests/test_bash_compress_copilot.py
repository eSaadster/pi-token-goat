def test_dispatch_routes_copilot_standalone() -> None:
    result = detect_from_command("copilot explain 'git stash'")
    assert result is not None
    f, _argv = result
    assert f.name == "copilot"


def test_dispatch_does_not_route_gh_copilot_to_standalone() -> None:
    result = detect_from_command("gh copilot explain 'git stash'")
    assert result is not None
    f, _argv = result
    assert f.name == "gh-copilot"


