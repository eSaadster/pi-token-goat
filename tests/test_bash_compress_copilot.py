class TestGhCopilotSuggestWithTarget:
    """GhCopilotFilter should keep the 'Suggested command:' header."""

    def setup_method(self) -> None:
        self.f = GhCopilotFilter()
        self.argv = ["gh", "copilot", "suggest", "--target", "shell", "list files"]

    def test_keeps_suggested_command_header(self) -> None:
        result = self.f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, self.argv)
        assert "Suggested command:" in result

    def test_keeps_the_actual_command(self) -> None:
        result = self.f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, self.argv)
        assert "git stash" in result

    def test_drops_welcome_banner(self) -> None:
        result = self.f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, self.argv)
        assert "Welcome to GitHub Copilot" not in result

    def test_drops_spinner(self) -> None:
        result = self.f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, self.argv)
        assert "Asking GitHub Copilot" not in result

    def test_drops_disclaimer(self) -> None:
        result = self.f.compress(_GH_COPILOT_SUGGEST_WITH_TARGET, "", 0, self.argv)
        assert "Disclaimer:" not in result
        assert "Always review" not in result


