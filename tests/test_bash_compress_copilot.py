def test_copilot_registered_in_filters() -> None:
    names = [f.name for f in FILTERS]
    assert "copilot" in names


def test_copilot_in_all_exports() -> None:
    from token_goat import bash_compress

    assert "CopilotFilter" in bash_compress.__all__


def test_copilot_filter_adjacent_to_gh_copilot_filter() -> None:
    """CopilotFilter should immediately follow GhCopilotFilter in FILTERS."""
    names = [f.name for f in FILTERS]
    gh_idx = names.index("gh-copilot")
    cop_idx = names.index("copilot")
    assert cop_idx == gh_idx + 1, (
        f"CopilotFilter (index {cop_idx}) should be immediately after "
        f"GhCopilotFilter (index {gh_idx})"
    )


