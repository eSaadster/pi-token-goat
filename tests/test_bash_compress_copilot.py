# ---------------------------------------------------------------------------
# CopilotFilter — matches()
# ---------------------------------------------------------------------------


class TestCopilotStandaloneMatches:
    def setup_method(self) -> None:
        self.f = CopilotFilter()

    def test_matches_bare_binary(self) -> None:
        assert self.f.matches(["copilot"])

    def test_matches_explain_subcommand(self) -> None:
        assert self.f.matches(["copilot", "explain", "git stash"])

    def test_matches_suggest_subcommand(self) -> None:
        assert self.f.matches(["copilot", "suggest", "list files"])

    def test_does_not_match_gh_copilot(self) -> None:
        # gh copilot is GhCopilotFilter's territory, not ours.
        assert not self.f.matches(["gh", "copilot", "explain"])

    def test_does_not_match_empty(self) -> None:
        assert not self.f.matches([])

    def test_does_not_match_unrelated_binary(self) -> None:
        assert not self.f.matches(["npm", "install"])


# ---------------------------------------------------------------------------
# CopilotFilter — compress()
# ---------------------------------------------------------------------------


class TestCopilotStandaloneCompress:
    def setup_method(self) -> None:
        self.f = CopilotFilter()
        self.argv = ["copilot", "explain", "git stash"]

    def _compress(self, stdout: str = _COPILOT_STANDALONE_EXPLAIN) -> str:
        return self.f.compress(stdout, "", 0, self.argv)

    def test_drops_banner(self) -> None:
        result = self._compress()
        assert "GitHub Copilot v1.0.3" not in result
        assert "Authenticated as: octocat" not in result

    def test_drops_spinner(self) -> None:
        result = self._compress()
        assert "Generating..." not in result

    def test_drops_disclaimer(self) -> None:
        result = self._compress()
        assert "Disclaimer:" not in result
        assert "Note:" not in result

    def test_keeps_explanation_body(self) -> None:
        result = self._compress()
        assert "git stash saves uncommitted changes" in result
        assert "git stash pop" in result
        assert "git stash list" in result

    def test_savings_at_least_25_percent(self) -> None:
        result = self._compress()
        original = len(_COPILOT_STANDALONE_EXPLAIN.encode())
        compressed = len(result.encode())
        savings = 1.0 - compressed / original
        assert savings >= 0.25, f"savings={savings:.2%} < 25%"


